    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (tblFarmacias.getSelectedRow()>= 0) {
            int idFarmacia = (int) tblFarmacias.getValueAt(tblFarmacias.getSelectedRow(), 0);
            try{
            frmNMFarmacia obj = new frmNMFarmacia(idFarmacia);
            obj.setTitle("Modificar Farmacia");
            obj.setModal(true);
            obj.setVisible(true);
            tblFarmacias.setModel(new Farmacia().GetAllModel());
            }catch(Exception e){
                JOptionPane.showMessageDialog(rootPane, e);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecciona una farmacia");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

