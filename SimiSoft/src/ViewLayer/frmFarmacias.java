    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (tblFarmacias.getSelectedRow()>= 0) {
            int idFarmacia = (int) tblFarmacias.getValueAt(tblFarmacias.getSelectedRow(), 0);
            Farmacia farmacia = new Farmacia();
            farmacia.setIdFarmacia(idFarmacia);
            if(farmacia.Delete()){
                JOptionPane.showMessageDialog(rootPane, "Farmacia eliminada correctamente");
            }else {
                JOptionPane.showMessageDialog(rootPane, "No ha sido posible eliminar la farmacia");
            }
            tblFarmacias.setModel(farmacia.GetAllModel());
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecciona una farmacia");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

