    public void llenarComboBoxFarmacias(){
        DefaultTableModel res = farmacia.GetAllModel();
        for (int i = 0; i < res.getRowCount(); i++) {
            comboFarmacia.addItem(String.valueOf(res.getValueAt(i, 1)));
        }
    }
    
