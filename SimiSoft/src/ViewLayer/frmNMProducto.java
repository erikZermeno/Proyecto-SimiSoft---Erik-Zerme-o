    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (txtNombre.getText().isEmpty() || txtStock.getValue() < 0 || txtCaducidad.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Ingresa todos los campos");
        } else {
            
            farmacia.setNombre(String.valueOf(comboFarmacia.getSelectedItem()));
            farmacia.GetByName();

            Date date = txtCaducidad.getDate();
            DateFormat f = new SimpleDateFormat("yyyy-MM-dd");
            String fecha = f.format(date);
            if (idProducto == 0) {
                if (new Producto(0,
                        txtNombre.getText(),
                        fecha,
                        txtStock.getValue(),
                        farmacia.getIdFarmacia(),
                        1).Add()) {
                    JOptionPane.showMessageDialog(rootPane, "Producto agregado correctamente!");
                    this.dispose();
                }
            } else {
                producto.setNombre(txtNombre.getText());
                producto.setCaducidad(fecha);
                producto.setStock(txtStock.getValue());
                producto.setIdFarmacia(farmacia.getIdFarmacia());
                producto.setActivo(1);
                if (producto.Update()) {
                    JOptionPane.showMessageDialog(rootPane, "Producto actualizado correctamente!");
                    this.dispose();
                }

            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

